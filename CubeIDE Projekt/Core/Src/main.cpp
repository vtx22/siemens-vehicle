int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_I2C1_Init();
  MX_USART1_UART_Init();
  MX_USART3_UART_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */
  //Fan Full Speed
  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5, GPIO_PIN_SET);

  //Create RAILCONTROL for toggling voltage rails
  RAILCONTROL rail = RAILCONTROL();
  rail.set24VRail(false);
  rail.setADJRail(false);

  //Create PRINTER for Debug Messages
  PRINTER printer = PRINTER(&huart3);
  printer.printString("PCB: BOOTING....");
  HAL_Delay(1000);

  printer.printString("Initializing INA226...");
  //Create INA226 Power Monitor Object
  INA226 ina = INA226(&hi2c1);

  printer.printString("Initializing LEDs...");
  //Create LED Object
  LED led = LED();


  led.setSTAT1(false);
  led.setSTAT2(true);

  led.setAllNeopixels(0, 0, 0);
  HAL_Delay(3000);
  for(uint8_t cnt = 0; cnt < 11; cnt++)
	{
	  led.setNeopixel(255,255,255, 10 + cnt);
	  led.setNeopixel(255,255,255, 10 - cnt);

	  if(cnt < 6)
	  {
		  led.setNeopixel(255,0,0, 21 + cnt);
		  led.setNeopixel(255,0,0, 27 + cnt);
	  }

	  HAL_Delay(50);
	}

  printer.printString("Waiting for a few seconds....");
  for(uint8_t cnt = 0; cnt < 3; cnt++)
  {
	  led.toggleSTAT(2);
	  HAL_Delay(1000);
  }
  led.setSTAT2(true);

  printer.printString("Initializing ADCs...");
  //Create ADC Object
  ADC adc = ADC(&hadc1);


  printer.printString("Initializing DS18B20...");
  //Create TIMER Object for DS18B20
  TIMER timer = TIMER(&htim2);
  //Create DS18B20 Object for On-Board Sensor
  DS18B20 tempPCB = DS18B20(&timer, GPIOB, GPIO_PIN_13);
  DS18B20 tempIOT = DS18B20(&timer, GPIOA, GPIO_PIN_15);

  printer.printString("Initializing UART1 Communication Handler...");
  //Create UART Handler
  UART uart = UART(&huart1, &ina, &printer, &tempPCB, &tempIOT, &led);
  RX_BUFFER = uart.RXBuffer;  //Make the pointer to RXBuffer globally available, needed for the ISR

  printer.printString("Initializing Servo...");
  //Create Servo Object
  SERVO servo = SERVO(&htim3);

  printer.printString("Initializing MPU6050 Gyroscope...");
  //Create MPU6050 Gyro Object
  MPU6050_t MPU6050;
  //Try to initialise Gyroscope Sensor
  uint8_t mpuInitTry = 0;
  bool gyroInitialised = true;

  while(MPU6050_Init(&hi2c1) == 1)
  {
	  if(mpuInitTry >= 5)
	  {
		  printer.printString("ERROR: MPU6050 Gyroscope initialization failed!");
		  led.setSTAT1(true);
		  gyroInitialised = false;
		  break;
	  }
	  mpuInitTry++;
  }

  printer.printString("Initializing SHT31 Temperature Sensor...");
  //Create Handle for SHT31 Sensor
  sht3x_handle_t handle = {
        .i2c_handle = &hi2c1,
        .device_address = SHT3X_I2C_DEVICE_ADDRESS_ADDR_PIN_LOW
  };
  //Try to initialise SHT31 Sensor
  bool shtInitialised = true;
  if(!sht3x_init(&handle))
  {
	  printer.printString("ERROR: SHT31 Sensor initialization failed!");
	  shtInitialised = false;
	  led.setSTAT1(true);
  }



  //ULTRA ultra = ULTRA(GPIOA, GPIO_PIN_12, GPIOA, GPIO_PIN_11, &timer);
  //WATCHDOG watchdog = WATCHDOG(&adc, &printer, &rail, &ina);




  printer.printString("Initializing Hardware Timer...");
  HAL_TIM_Base_Start(&htim1);
  HAL_TIM_Base_Start(&htim2);
  HAL_TIM_Base_Start(&htim3);
  HAL_TIM_Base_Start(&htim4);
  //watchdog.fullSelftest();
  //HAL_Delay(1000);


  printer.printString("Success! Entering main loop!");

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  uint8_t batCnt = 0;
  uint8_t tempCnt = 0;
  uint32_t ledTicks = HAL_GetTick();
  uint32_t ds18b20Ticks = ledTicks;


  tempPCB._firstREQ();
  tempIOT._firstREQ();

  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	    if (parse_uart)
	    {
	      uart.parseMessage();
	      parse_uart = false;

	      led.toggleSTAT(2);
	    }

	    if(HAL_GetTick() - ds18b20Ticks >= 800)
	    {
	    	tempPCB._lastTemp = tempPCB._secREQ();
	    	tempPCB._firstREQ();
	    	tempIOT._lastTemp = tempIOT._secREQ();
	    	tempIOT._firstREQ();
	    	ds18b20Ticks = HAL_GetTick();
	    }


	    if(HAL_GetTick() - ledTicks >= 50)
	    {
	    	ledTicks = HAL_GetTick();
	    	led._updateCNT++;
			switch(led._mode)
			{
			case LED_BLINK_RIGHT:
				led.animateBlink(true, false);
				break;
			case LED_BLINK_LEFT:
				led.animateBlink(false, true);
				break;
			case LED_BLINK_BOTH:
				led.animateBlink(true, true);
				break;
			case LED_IDLE:
				break;
			default:
				uint8_t baseColor[3]={128,0,0};
				led.animateSine(baseColor, 0.2, 200, 1);
				break;
			}
	    }

	    tempCnt++;

	    uart.sendBATstatus();

	    if(gyroInitialised)
		{
	    	HAL_Delay(UART_DATA_DELAY);
			MPU6050_Read_All(&hi2c1, &MPU6050);
			uart.sendGYROAngle(MPU6050.KalmanAngleX, MPU6050.KalmanAngleY);
			HAL_Delay(UART_DATA_DELAY / 2);
			uart.sendGYROAccel(MPU6050.Ax, MPU6050.Ay, MPU6050.Az);
			HAL_Delay(UART_DATA_DELAY / 2);
			uart.sendGYROVeloc(MPU6050.Gx, MPU6050.Gy, MPU6050.Gz);
		}
	    if(tempCnt >= 10)
	    {
	    	float temperature=0, humidity = 0;
			if(shtInitialised)
			{
				sht3x_read_temperature_and_humidity(&handle, &temperature, &humidity);
			}
	    	uart.sendTEMPstatus(temperature, humidity);
	    	tempCnt = 0;
	    }

	    HAL_Delay(UART_DATA_DELAY);
	    /*
	    uint8_t baseColor[3] = {0,0,128};
	    if(TIM4->CNT >= 50) {
	    	led._updateCNT++;
	    	led.animateSine(baseColor, 0.2, 150, 1);
	    	TIM4->CNT = 0;
	    }
		*/
	    /*
	    for(uint8_t cnt = 5; cnt < 22 - 5; cnt++)
	    {
	    	led.setNeopixel(255,255,255, cnt);
	    }

	    uint8_t color[3] = {255,100,0};
	    for(uint8_t cnt = 0; cnt < 5; cnt++)
	    {
	    	led.setNeopixel(color, 5-cnt);
	    	led.setNeopixel(color, 21-5+cnt);
	    	HAL_Delay(50);
	    }
	    HAL_Delay(700);
	    for(uint8_t cnt = 0; cnt < 5; cnt++)
		{
			led.setNeopixel(0,0,0, 5-cnt);
			led.setNeopixel(0,0,0, 21-5+cnt);
		}

	    */






  }
  /* USER CODE END 3 */
}

